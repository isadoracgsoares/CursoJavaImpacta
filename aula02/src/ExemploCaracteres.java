
public class ExemploCaracteres {
	
	public static void main(String[] args) {
		
		//tipo caracter da tabela UNICODE (16 bits - 65535 caracteres)
		char letra = 90;
		System.out.println("Letra informada é: " + letra);
		System.out.printf("Letra informada é: %s \n", letra);
		
		char letra2 = 'Z'; //aspas simples
		System.out.printf("Letra informada é: %s \n", letra2);
		
		char letra3 = 'A'; //aspas simples
		System.out.printf("Letra informada é: %s \n", letra3);
	}
	
}

